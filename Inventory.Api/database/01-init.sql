    ticket_item_cen uuid NOT NULL,
    product_id integer NOT NULL,
    product_cen uuid NOT NULL,
    quantity numeric(12,2) NOT NULL,
    status text NOT NULL,
    notes text,
    created_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "PK_command_items" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_command_items_commands_command_id" FOREIGN KEY (command_id) REFERENCES sales.commands ("Id") ON DELETE CASCADE
