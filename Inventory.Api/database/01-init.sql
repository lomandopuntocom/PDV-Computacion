CREATE TABLE IF NOT EXISTS sales.commands (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    cen uuid NOT NULL DEFAULT gen_random_uuid(),
    company_id integer NOT NULL,
    company_cen uuid NOT NULL,
    location_id integer NOT NULL,
    location_cen uuid NOT NULL,
    ticket_id integer NOT NULL,
    ticket_cen uuid NOT NULL,
    station_id integer NOT NULL,
    station_cen uuid NOT NULL,
    command_number text NOT NULL,
    status text NOT NULL,
    is_reorder boolean NOT NULL DEFAULT false,
    created_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    sent_at timestamp with time zone,
    ready_at timestamp with time zone,
    updated_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "PK_commands" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_commands_Ticket" FOREIGN KEY (ticket_id) REFERENCES sales."Ticket" ("Id") ON DELETE CASCADE
