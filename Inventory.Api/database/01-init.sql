CREATE TABLE IF NOT EXISTS sales."Company" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "Name" text NOT NULL,
    CONSTRAINT "PK_Company" PRIMARY KEY ("Id")
);

CREATE TABLE IF NOT EXISTS sales."Location" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "CompanyId" integer NOT NULL,
    "CompanyCen" uuid NOT NULL,
    "Name" text NOT NULL,
    CONSTRAINT "PK_Location" PRIMARY KEY ("Id")
);

CREATE TABLE IF NOT EXISTS sales."Vendor" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "CompanyId" integer NOT NULL,
    "CompanyCen" uuid NOT NULL,
    "Name" text NOT NULL,
    "Email" text,
    "Phone" text,
    "IsWaiter" boolean NOT NULL DEFAULT false,
    "Active" boolean NOT NULL DEFAULT true,
    "CreatedAt" timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "UpdatedAt" timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "PK_Vendor" PRIMARY KEY ("Id")
);

CREATE TABLE IF NOT EXISTS sales."Ticket" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "CompanyId" integer NOT NULL,
    "CompanyCen" uuid NOT NULL,
    "LocationId" integer NOT NULL,
    "LocationCen" uuid NOT NULL,
    "TicketNumber" text NOT NULL,
    "VendorId" integer,
    "VendorCen" uuid,
    "TableCode" text,
    "Status" text NOT NULL,
    CONSTRAINT "PK_Ticket" PRIMARY KEY ("Id")
);

CREATE TABLE IF NOT EXISTS sales."TicketItem" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "TicketId" integer NOT NULL,
    "TicketCen" uuid NOT NULL,
    "ProductId" integer NOT NULL,
    "ProductCen" uuid NOT NULL,
    "Quantity" numeric(12,2) NOT NULL DEFAULT 1,
    "UnitPrice" numeric(12,2) NOT NULL DEFAULT 0,
    "Status" text NOT NULL,
    "Notes" text,
    CONSTRAINT "PK_TicketItem" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_TicketItem_Ticket_TicketId" FOREIGN KEY ("TicketId") REFERENCES sales."Ticket" ("Id") ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS sales."Payment" (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
    "Cen" uuid NOT NULL DEFAULT gen_random_uuid(),
    "TicketId" integer NOT NULL,
    "TicketCen" uuid NOT NULL,
    "PaymentMethod" text NOT NULL,
    "Amount" numeric(12,2) NOT NULL,
    "Reference" text,
    "PaidBy" text,
    "CreatedAt" timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "UpdatedAt" timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "PK_Payment" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Payment_Ticket_TicketId" FOREIGN KEY ("TicketId") REFERENCES sales."Ticket" ("Id") ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS sales.tax_configuration (
    "Id" integer GENERATED BY DEFAULT AS IDENTITY,
